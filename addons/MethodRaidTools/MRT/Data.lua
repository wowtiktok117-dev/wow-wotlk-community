	local function appendLocale(localeKey, entries)
		D.SubzoneTextToBossID[localeKey] = D.SubzoneTextToBossID[localeKey] or {}
		local t = D.SubzoneTextToBossID[localeKey]
		for k, v in pairs(entries) do
